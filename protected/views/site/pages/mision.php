<h1>Visión y Misión</h1>

<h2> Visión </h2>
Ser reconocida por su excelencia, por implementar un proceso de aprendizaje centrado en el estudiante que forma profesionales competentes técnica, humana y socialmente, con una gestión curricular efectiva y eficiente que fomenta el emprendimiento, la creatividad y la innovación en los estudiantes.
<p></p>
<h2>   Misión</h2>
Formar profesionales competentes e integrales capaces de gestionar proyectos en el ámbito de la informática a fin de resolver problemas del medio empresarial y de la sociedad a través del uso de tecnologías de información, contribuyendo al desarrollo del país y de la región.  Asimismo, ofrece un espacio de desarrollo a los jóvenes posibilitando la movilidad social.
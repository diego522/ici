<h1>Perfil de Egreso</h1>

<p style="text-align: justify;">
<p>El Ingeniero Civil en Informática de la Universidad del Bío-Bío es un profesional 

    capacitado para comprender la problemática organizacional en el ámbito de la 

    informática y, a partir de ello, plantear y desarrollar soluciones pertinentes. Lo 

    anterior se traduce en la formulación, evaluación, e implementación de proyectos 

    de tecnologías de información y comunicación. 
</p>
<p>Este profesional trabaja colaborativamente, lidera equipos multidisciplinarios en 

    forma ética, proactiva, responsable y autónoma, respetando la diversidad de 

    quienes conforman los equipos en que participa.</p>

<p>El sello distintivo del egresado de esta carrera lo constituye su conocimiento de la 

    gestión organizacional y su disposición hacia el logro de metas, optimizando los 

    recursos informáticos disponibles en la organización en la que se desenvuelva.

    La formación profesional del egresado contempla las áreas de: Ciencias Básicas, 

    Ciencias de la Ingeniería, Sistemas Computacionales, Ingeniería de Software y 

    Base de Datos y Gestión de Informática.</p>

<p>Además, el egresado de esta carrera se distingue por el compromiso permanente 

    con su aprendizaje, maneja el idioma inglés para apoyar el desarrollo de su 

    trabajo y asume su quehacer profesional y ciudadano, sirviendo a la sociedad con 

    innovación, excelencia y responsabilidad social.</p></p>